/*
  SQL 종류
  
  1. DDL (Data Definition Language)
    1) 데이터 정의어
    2) 데이터 베이스 객체(데이터베이스, 테이블, 사용자) 등의 정보를 다루는 언어
    3) 종류
      (1) CREATE: 생성
      (2) ALTER: 수정
      (3) DROP: 삭제
      (4) TRUNC: 삭제의 한 종류
  2. DML (Data Manipulation Language)
    1) 데이터 조작어
    2) 실제 데이터를 다루는 언어
    3) 종류
      (1) INSERT: 삽입
      (2) UPDATE: 갱신
      (3) DELETE: 삭제
  3. DQL (Data Query Language)
    1) 데이터 질의어
    2) 데이터를 검색 할 때 사용 하는 언어
    3) 종류: SELECT
  4. DCL (Data Control Language)
    1) 데이터 제어어
    2) 데이터를 제어 할 때 사용 하는 언어
    3) 종류
      (1) GRANT: 승인
      (2) REVOKE: 승인 해제
  5. TCL (transaction Control Language)
    1) 트랜잭션 제어어
    2) 트랜잭션을 처리 할 때 사용 하는 언어
    3) 종류
     (1) COMMIT: 트랜잭션 성공
     (2) ROLLBACK: 트랜잭션 실패 (작업 내용 취소)
*/